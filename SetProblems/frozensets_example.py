def demonstrate_frozenset():
    """
    Description:
        This function demonstrates the use of frozensets, which are immutable sets.
    Parameters:
        None
    Return:
        None
    """
    frozenset1 = frozenset([1, 2, 3, 4])
    print("Frozenset:", frozenset1)

def main():
    demonstrate_frozenset()

if __name__ == "__main__":
    main()
