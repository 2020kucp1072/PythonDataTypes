def iterate_set():
    """
    Description:
        This function iterates over a set and prints its elements.
    Parameters:
        None
    Return:
        None
    """
    my_set = {1, 2, 3, 4, 5}
    for element in my_set:
        print(element)

def main():
    iterate_set()

if __name__ == "__main__":
    main()
